const DIRECTION = [[0, 1], [1, 0], [-1, 0], [0, -1]]
const queue = []
let answer = 0

for (let i = 0; i < N; i++) {
    for (let j = 0; j < M; j++) {
        if (FRAME[i][j]) continue;

        queue.push([i, j])
        answer += 1;

        while(queue.length) {
            const [r, c] = queue.shift()

            for (let [dy, dx] of DIRECTION) {
                const y = dy + r;
                const x = dx + c;

                if (0 <= y && y < N && 0 <= x && x < M && !FRAME[y][x]) {
                    queue.push([y, x])
                    FRAME[y][x] = 2
                }
            }
        }
     }
}

